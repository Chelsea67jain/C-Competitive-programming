/* Typecasting is convertion from one data type to another */

#include <iostream>
using namespace std;

int main(){
	 int a='a';
	 cout<<a<<endl;//97  the ascii value of a is 97 
	 char c=98;
	 cout<<c<<endl; // b  98 is the ascii value of character b
	 
	 char d=123456;
	 cout<<d<<endl;//@ 
	 /*the maximum value the character can store is 2^8-1 so the compiler converts 123456 to nearest value of character i.e
	 64 and 64 is the ascii code of @ */
	 
	
	 
	 
}

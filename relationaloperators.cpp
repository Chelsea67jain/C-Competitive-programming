/*
relational operators: these operators take two operants and returns a boolean value true or false
>,<,>=,<=,!=,=,==

*/

#include <iostream>
using namespace std;

int main(){
	int a=2,b=3;
	cout<<(a==b)<<endl;//0
	cout<<(a>b)<<endl;//0
	cout<<(a<b)<<endl;//1
	cout<<(a>=b)<<endl;//0
	cout<<(a<=b)<<endl;//1
	cout<<(a!=b)<<endl;//1
	cout<<(a=b)<<endl;//3
}

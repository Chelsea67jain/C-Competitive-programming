/*
Arithmetic operators : +,-,*,/
int/int is integer
float/int is float
double/int is double
*/

#include <iostream>
using namespace std;

int main(){
	int a=2/5;
cout<<a<<endl;//0
int b=2.0/5; 
cout<<b<<endl;//0
cout<<2.0/5<<endl;//0.4

int c=2.54/5;
cout<<c<<endl;//0
cout<<2.54/5<<endl;//0.508
	
	
}


